#ifndef STATEMANAGER_H
#define STATEMANAGER_H

#include <SDL.h>
#include "NGLDraw.h"
#include "menu.h"

enum {GAME, PAUSED, WINNER, LOSER, MENU};

class StateManager
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    StateManager(int w, int h);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    ~StateManager();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void draw(int _w, int _h, float _delta);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void mouseMoveEvent (const SDL_MouseMotionEvent &_event);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void mousePressEvent (const SDL_MouseButtonEvent &_event);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void mouseReleaseEvent (const SDL_MouseButtonEvent &_event);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void wheelEvent(const SDL_MouseWheelEvent &_event);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void keyboardPressEvent(const SDL_Event &_event);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void resize(int _w, int _h);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor this will have a valid OpenGL context so we can create gl stuff
    //----------------------------------------------------------------------------------------------------------------------
    void changeState();

private:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief creates an object of the NGLDraw class in order to start the game
    //----------------------------------------------------------------------------------------------------------------------
    NGLDraw gameState;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief creates an object of the Menu class in order to start the menu
    //----------------------------------------------------------------------------------------------------------------------
    Menu GameMenu;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief variable to determine current 'state' the game is in
    //----------------------------------------------------------------------------------------------------------------------
    int m_state;

};

#endif // STATEMANAGER_H

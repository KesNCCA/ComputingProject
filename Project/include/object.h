#ifndef OBJECT_H
#define OBJECT_H

#include <SDL.h>
#include <ngl/Transformation.h>
#include <ngl/Mat4.h>
#include <ngl/Vec3.h>
#include <ngl/ShaderLib.h>
#include <ngl/NGLInit.h>

class Object
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor, initialises all game object heights
    //----------------------------------------------------------------------------------------------------------------------
    Object();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief dtor
    //----------------------------------------------------------------------------------------------------------------------
    ~Object();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief drawing object
    //----------------------------------------------------------------------------------------------------------------------
    void Draw(ngl::Mat4 m_view, ngl::Mat4 m_mouseGlobalTX, ngl::Mat4 m_project);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief keyboard event handling
    //----------------------------------------------------------------------------------------------------------------------
    void keyboardPressEvent(const SDL_Event &_event);

protected:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief the model position for mouse movement
    //----------------------------------------------------------------------------------------------------------------------
    void loadMatricesToShader(ngl::Mat4 m_view, ngl::Mat4 m_mouseGlobalTX, ngl::Mat4 m_project);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief Object position
    //----------------------------------------------------------------------------------------------------------------------
    ngl::Transformation m_transform;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief object rotation
    //----------------------------------------------------------------------------------------------------------------------
    ngl::Transformation m_rotate;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief Height position of all objects within game
    //----------------------------------------------------------------------------------------------------------------------
    float m_HeightPos;
};

#endif // OBJECT_H
